package cn.com.cdboost.collect.dto;

import java.math.BigDecimal;

/**
 * 能效分析汇总
 */
public class VillageElectricityDto {
    /**
     *小区标识
     */
    private String orgNo;
    /**
     *小区名称
     */
    private String orgName;
    /**
     *电表数量
     */
    private Integer ammeterNum;
    /**
     *照明插座（KW/H）
     */
    private BigDecimal socketElectricity;
    /**
     *空调（KW/H）
     */
    private BigDecimal airElectricity;
    /**
     *动力（KW/H）
     */
    private BigDecimal powerElectricity;
    /**
     *特殊用电（KW/H）
     */
    private BigDecimal specialElectricity;
    /**
     *位置信息
     */
    private String villageAddr;
    /**
     *台区数
     */
    private Integer platformAreaNum;

    public String getOrgNo() {
        return orgNo;
    }

    public void setOrgNo(String orgNo) {
        this.orgNo = orgNo;
    }

    public String getOrgName() {
        return orgName;
    }

    public void setOrgName(String orgName) {
        this.orgName = orgName;
    }

    public Integer getAmmeterNum() {
        return ammeterNum;
    }

    public void setAmmeterNum(Integer ammeterNum) {
        this.ammeterNum = ammeterNum;
    }

    public BigDecimal getSocketElectricity() {
        return socketElectricity;
    }

    public void setSocketElectricity(BigDecimal socketElectricity) {
        this.socketElectricity = socketElectricity;
    }

    public BigDecimal getAirElectricity() {
        return airElectricity;
    }

    public void setAirElectricity(BigDecimal airElectricity) {
        this.airElectricity = airElectricity;
    }

    public BigDecimal getPowerElectricity() {
        return powerElectricity;
    }

    public void setPowerElectricity(BigDecimal powerElectricity) {
        this.powerElectricity = powerElectricity;
    }

    public BigDecimal getSpecialElectricity() {
        return specialElectricity;
    }

    public void setSpecialElectricity(BigDecimal specialElectricity) {
        this.specialElectricity = specialElectricity;
    }

    public String getVillageAddr() {
        return villageAddr;
    }

    public void setVillageAddr(String villageAddr) {
        this.villageAddr = villageAddr;
    }

    public Integer getPlatformAreaNum() {
        return platformAreaNum;
    }

    public void setPlatformAreaNum(Integer platformAreaNum) {
        this.platformAreaNum = platformAreaNum;
    }
}
