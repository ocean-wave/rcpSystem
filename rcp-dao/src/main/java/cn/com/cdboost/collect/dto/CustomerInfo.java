package cn.com.cdboost.collect.dto;

import java.io.Serializable;
import java.util.Date;

/**
 * 补抄工单详情中，客户档案信息
 */
public class CustomerInfo implements Serializable{
    private static final long serialVersionUID = -3456492138435927714L;

    /**
     * 采集数据标识
     */
    private String groupGuid;

    /**
     * 客户名称
     */
    private String customerName;

    /**
     * 客户唯一标识
     */
    private String customerNo;

    /**
     * 客户地址
     */
    private String customerAddr;

    /**
     * 客户电话
     */
    private String customerContact;

    /**
     * 表的唯一标识
     */
    private String cno;

    /**
     * 表计户号
     */
    private String meterUserNo;

    /**
     * 采集人员
     */
    private String userName;

    /**
     * 采集标识 0未采集 1已采集
     */
    private String flag;

    /**
     * 采集顺序
     */
    private String collectSort;

    /**
     * 表的规约 1 标识97 30 标识07
     */
    private String commRule;

    /**
     * 表地址
     */
    private String commAddr;

    /**
     * 采集方式 1标识红外 2标识手动采集
     */
    private String dataSrc;

    /**
     * 电表异常码
     */
    private Integer errCode;

    /**
     * 补抄时间
     */
    private Date suppTime;

    /**
     * 安装位置
     */
    private String installAddr;

    public String getGroupGuid() {
        return groupGuid;
    }

    public void setGroupGuid(String groupGuid) {
        this.groupGuid = groupGuid;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getCustomerNo() {
        return customerNo;
    }

    public void setCustomerNo(String customerNo) {
        this.customerNo = customerNo;
    }

    public String getCustomerAddr() {
        return customerAddr;
    }

    public void setCustomerAddr(String customerAddr) {
        this.customerAddr = customerAddr;
    }

    public String getCno() {
        return cno;
    }

    public void setCno(String cno) {
        this.cno = cno;
    }

    public String getMeterUserNo() {
        return meterUserNo;
    }

    public void setMeterUserNo(String meterUserNo) {
        this.meterUserNo = meterUserNo;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }

    public String getCollectSort() {
        return collectSort;
    }

    public void setCollectSort(String collectSort) {
        this.collectSort = collectSort;
    }

    public String getCommRule() {
        return commRule;
    }

    public void setCommRule(String commRule) {
        this.commRule = commRule;
    }

    public String getCommAddr() {
        return commAddr;
    }

    public void setCommAddr(String commAddr) {
        this.commAddr = commAddr;
    }

    public String getDataSrc() {
        return dataSrc;
    }

    public void setDataSrc(String dataSrc) {
        this.dataSrc = dataSrc;
    }

    public Integer getErrCode() {
        return errCode;
    }

    public void setErrCode(Integer errCode) {
        this.errCode = errCode;
    }

    public String getCustomerContact() {
        return customerContact;
    }

    public void setCustomerContact(String customerContact) {
        this.customerContact = customerContact;
    }

    public Date getSuppTime() {
        return suppTime;
    }

    public void setSuppTime(Date suppTime) {
        this.suppTime = suppTime;
    }

    public String getInstallAddr() {
        return installAddr;
    }

    public void setInstallAddr(String installAddr) {
        this.installAddr = installAddr;
    }
}
