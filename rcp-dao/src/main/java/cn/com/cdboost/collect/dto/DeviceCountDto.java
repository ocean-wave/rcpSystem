package cn.com.cdboost.collect.dto;

public class DeviceCountDto {
    /**
     * 是否通断名称
     */
    private String isOn;
    /**
     *方案执行日期
     */
    private String schemeDate;
    /**
     * 方案执行时间
     */
    private String schemeTime;

    public String getIsOn() {
        return isOn;
    }

    public void setIsOn(String isOn) {
        this.isOn = isOn;
    }

    public String getSchemeDate() {
        return schemeDate;
    }

    public void setSchemeDate(String schemeDate) {
        this.schemeDate = schemeDate;
    }

    public String getSchemeTime() {
        return schemeTime;
    }

    public void setSchemeTime(String schemeTime) {
        this.schemeTime = schemeTime;
    }
}
