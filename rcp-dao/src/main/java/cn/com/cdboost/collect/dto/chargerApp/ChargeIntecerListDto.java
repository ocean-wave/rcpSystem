package cn.com.cdboost.collect.dto.chargerApp;

import java.math.BigDecimal;
import java.util.List;

public class ChargeIntecerListDto {
    /**
     * 剩余金额
     */
    private BigDecimal remainAmount;
    /**
     * 已充值金额
     */
    private BigDecimal chargedMoney;
    /**
     * 方案列表
     */
    private List<ChargeListDto> list;

    public BigDecimal getRemainAmount() {
        return remainAmount;
    }

    public void setRemainAmount(BigDecimal remainAmount) {
        this.remainAmount = remainAmount;
    }

    public BigDecimal getChargedMoney() {
        return chargedMoney;
    }

    public void setChargedMoney(BigDecimal chargedMoney) {
        this.chargedMoney = chargedMoney;
    }

    public List<ChargeListDto> getList() {
        return list;
    }

    public void setList(List<ChargeListDto> list) {
        this.list = list;
    }
}
