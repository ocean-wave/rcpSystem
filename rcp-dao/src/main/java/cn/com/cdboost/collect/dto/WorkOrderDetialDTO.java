package cn.com.cdboost.collect.dto;

import com.alibaba.fastjson.annotation.JSONField;

import java.util.Date;

/**
 * @author xzy
 * @desc 补抄工单列表数据传输对象
 * @create 2017/9/12 0012
 **/
public class WorkOrderDetialDTO {

    private String taskContent;	    // 工单内容
    @JSONField(format = "yyyy-MM-dd HH:mm:ss")
    private Date startTime;	        // 工单执行时间
    @JSONField(format = "yyyy-MM-dd HH:mm:ss")
    private Date endTime;	        // 工单执行时间
    private String taskNo;	        //工单编号
    @JSONField(format = "yyyy-MM-dd HH:mm:ss")
    private Date createTime;	    //创建时间
    private String createUserId;	//创建人员ID
    private String userName;	    //创建人员
    @JSONField(format = "yyyy-MM-dd HH:mm:ss")
    private Date updateTime;	    //更新时间
    private String meterCount;	    //采集数量
    private String orgName;	        //组织结构
    private int flag;	        //0 标识待处理 10 标识掌机已请求 20工单已完成
    private int complateCount;	        //完成数量

    /**
     * 请求人数
     */
    private int queryCount;

    /**
     * 执行人员id
     */
    private Integer runtor;



    public String getTaskContent() {
        return taskContent;
    }

    public void setTaskContent(String taskContent) {
        this.taskContent = taskContent;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public String getTaskNo() {
        return taskNo;
    }

    public void setTaskNo(String taskNo) {
        this.taskNo = taskNo;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getCreateUserId() {
        return createUserId;
    }

    public void setCreateUserId(String createUserId) {
        this.createUserId = createUserId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public String getMeterCount() {
        return meterCount;
    }

    public void setMeterCount(String meterCount) {
        this.meterCount = meterCount;
    }

    public String getOrgName() {
        return orgName;
    }

    public void setOrgName(String orgName) {
        this.orgName = orgName;
    }

    public int getFlag() {
        return flag;
    }

    public void setFlag(int flag) {
        this.flag = flag;
    }

    public int getComplateCount() {
        return complateCount;
    }

    public void setComplateCount(int complateCount) {
        this.complateCount = complateCount;
    }

    public int getQueryCount() {
        return queryCount;
    }

    public void setQueryCount(int queryCount) {
        this.queryCount = queryCount;
    }

    public Integer getRuntor() {
        return runtor;
    }

    public void setRuntor(Integer runtor) {
        this.runtor = runtor;
    }
}
