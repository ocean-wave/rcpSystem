package cn.com.cdboost.collect.emums;

/**
 * 存储过程结果返回值，1都是成功，非1标识失败
 */
public enum ProcedureErrorCode {
    SUCCESS(1,"成功");


    /**
     * 编码
     */
    private Integer code;

    /**
     * 描述
     */
    private String desc;

    ProcedureErrorCode(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
