package cn.com.cdboost.collect.dto;

import java.math.BigDecimal;

/**
 * @author xzy
 * @desc 汇总当前月的的电、水、气量和费用返回值的数据传输对象
 * @create 2017/8/29 0029
 **/
public class SumDataGetDTO {

    private String deviceType;          // 设备类型
    private BigDecimal sumPR;               // 汇总电量值
    private BigDecimal sumRemainAmount;     // 汇总剩余金额
    private String cltMonth;            // 月份

    public String getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(String deviceType) {
        this.deviceType = deviceType;
    }

    public BigDecimal getSumPR() {
        return sumPR;
    }

    public void setSumPR(BigDecimal sumPR) {
        this.sumPR = sumPR;
    }

    public BigDecimal getSumRemainAmount() {
        return sumRemainAmount;
    }

    public void setSumRemainAmount(BigDecimal sumRemainAmount) {
        this.sumRemainAmount = sumRemainAmount;
    }

    public String getCltMonth() {
        return cltMonth;
    }

    public void setCltMonth(String cltMonth) {
        this.cltMonth = cltMonth;
    }
}
