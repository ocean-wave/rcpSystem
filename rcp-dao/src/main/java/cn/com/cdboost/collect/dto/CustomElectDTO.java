package cn.com.cdboost.collect.dto;

import java.math.BigDecimal;

/**
 * @author wt
 * @desc 用户用电排行
 * @create 2017/8/29 0029
 **/
public class CustomElectDTO {

    /**
     * 户号
     */
    private String meterUserNo;
    /**
     * 用电量
     */
    private BigDecimal pr0;
    /**
     * 户名
     */
    private String customerName;
    /**
     * 用电地址(设备安装地址)
     */
    private String installAddr;
    /**
     * 楼栋
     */

    private String buildingNo;
    /**
     * 用电用水用气类型
     */
    private String dictItemName;
    /**
     * 变压器号
     */
    private String transformerNo;
    /**
     * 用能数据
     */

    private BigDecimal elecDatas;

    public BigDecimal getElecDatas() {
        return elecDatas;
    }

    public void setElecDatas(BigDecimal elecDatas) {
        this.elecDatas = elecDatas;
    }

    public String getMeterUserNo() {
        return meterUserNo;
    }

    public void setMeterUserNo(String meterUserNo) {
        this.meterUserNo = meterUserNo;
    }

    public BigDecimal getPr0() {
        return pr0;
    }

    public void setPr0(BigDecimal pr0) {
        this.pr0 = pr0;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getInstallAddr() {
        return installAddr;
    }

    public void setInstallAddr(String installAddr) {
        this.installAddr = installAddr;
    }

    public String getBuildingNo() {
        return buildingNo;
    }

    public void setBuildingNo(String buildingNo) {
        this.buildingNo = buildingNo;
    }

    public String getDictItemName() {
        return dictItemName;
    }

    public void setDictItemName(String dictItemName) {
        this.dictItemName = dictItemName;
    }

    public String getTransformerNo() {
        return transformerNo;
    }

    public void setTransformerNo(String transformerNo) {
        this.transformerNo = transformerNo;
    }
}
