package cn.com.cdboost.collect.dto;

import com.alibaba.fastjson.annotation.JSONField;

import java.math.BigDecimal;

/**
 * @author wt
 * @desc 电量电费返回
 * @create 2017/7/11 0011
 **/
public class ElectDetailDto   {
    @JSONField(name = "feePayTime")
    private String create_time;
    @JSONField(name="meterUserNo")
    private String meter_user_no;
    @JSONField(name="deviceNo")
    private String device_no;
    @JSONField(name="price")
    private BigDecimal unit_price;
    @JSONField(name="remainAmount")
    private BigDecimal remain_amount;
    @JSONField(name="feePayType")
    private String calc_data_type;
    @JSONField(name = "startDegree")
    private BigDecimal last_day_read_value;
    @JSONField(name = "endDegree")
    private BigDecimal read_value;
    @JSONField(name = "electricTotal")
    private BigDecimal day_eq_value;
    @JSONField(name = "feePayMoney")
    private BigDecimal deduct_money;
    private String  ratio;

    public String getRatio() {
        return ratio;
    }

    public void setRatio(String ratio) {
        this.ratio = ratio;
    }

    public String getCalc_data_type() {
        return calc_data_type;
    }

    public void setCalc_data_type(String calc_data_type) {
        this.calc_data_type = calc_data_type;
    }

    public BigDecimal getRead_value() {
        return read_value;
    }

    public void setRead_value(BigDecimal read_value) {
        this.read_value = read_value;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getMeter_user_no() {
        return meter_user_no;
    }

    public void setMeter_user_no(String meter_user_no) {
        this.meter_user_no = meter_user_no;
    }

    public String getDevice_no() {
        return device_no;
    }

    public void setDevice_no(String device_no) {
        this.device_no = device_no;
    }

    public BigDecimal getUnit_price() {
        return unit_price;
    }

    public void setUnit_price(BigDecimal unit_price) {
        this.unit_price = unit_price;
    }

    public BigDecimal getRemain_amount() {
        return remain_amount;
    }

    public void setRemain_amount(BigDecimal remain_amount) {
        this.remain_amount = remain_amount;
    }



    public BigDecimal getLast_day_read_value() {
        return last_day_read_value;
    }

    public void setLast_day_read_value(BigDecimal last_day_read_value) {
        this.last_day_read_value = last_day_read_value;
    }



    public BigDecimal getDay_eq_value() {
        return day_eq_value;
    }

    public void setDay_eq_value(BigDecimal day_eq_value) {
        this.day_eq_value = day_eq_value;
    }

    public BigDecimal getDeduct_money() {
        return deduct_money;
    }

    public void setDeduct_money(BigDecimal deduct_money) {
        this.deduct_money = deduct_money;
    }
}
