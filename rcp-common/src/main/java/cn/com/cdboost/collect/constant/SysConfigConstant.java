package cn.com.cdboost.collect.constant;

/**
 * 系统配置参数常量
 */
public enum SysConfigConstant {

    FRONT_PROCESSOR_IP(1,"serviceIp"),
    FRONT_PROCESSOR_PORT(1,"servicePort"),
    FRONT_PROCESSOR_APN(1,"apn"),
    SYSTEM_NAME(2,"sysName"),
    BALANCE_TIME(2,"balanceTime"),
    DB_VERSION(2,"version"),
    PAY_ADDR(3,"payAddr"),
    COMPANY_NAME(3,"companyName"),
    APP_EUI(5,"AppEUI"),
    APP_KEY(5,"AppKey"),
    WEB_SOCKET_URL(6,"webSocketUrl"),
    COPY_RIGHT(7,"copyRight");
    /**
     * 配置值
     */
    private Integer configType;

    /**
     * 配置名称
     */
    private String configName;

    SysConfigConstant(Integer configType, String configName) {
        this.configType = configType;
        this.configName = configName;
    }

    public Integer getConfigType() {
        return configType;
    }

    public void setConfigType(Integer configType) {
        this.configType = configType;
    }

    public String getConfigName() {
        return configName;
    }

    public void setConfigName(String configName) {
        this.configName = configName;
    }
}
