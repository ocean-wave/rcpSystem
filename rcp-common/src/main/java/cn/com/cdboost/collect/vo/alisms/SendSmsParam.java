package cn.com.cdboost.collect.vo.alisms;

/**
 * 阿里云发送短信参数
 */
public class SendSmsParam extends SendSmsBaseParam{
    /**
     * 要发送的手机号码,多个手机号码以逗号分隔，最多1000个
     */
    private String phoneMobiles;

    /**
     * 短信签名
     */
    private String signName;

    /**
     * 短信模板
     */
    private String templateCode;

    /**
     * 模板中的变量替换JSON串
     */
    private String templateParam;

    /**
     * 提供给业务方扩展字段,最终在短信回执消息中将此值带回给调用者
     */
    private String outId;

    public String getPhoneMobiles() {
        return phoneMobiles;
    }

    public void setPhoneMobiles(String phoneMobiles) {
        this.phoneMobiles = phoneMobiles;
    }

    public String getSignName() {
        return signName;
    }

    public void setSignName(String signName) {
        this.signName = signName;
    }

    public String getTemplateCode() {
        return templateCode;
    }

    public void setTemplateCode(String templateCode) {
        this.templateCode = templateCode;
    }

    public String getTemplateParam() {
        return templateParam;
    }

    public void setTemplateParam(String templateParam) {
        this.templateParam = templateParam;
    }

    public String getOutId() {
        return outId;
    }

    public void setOutId(String outId) {
        this.outId = outId;
    }
}
