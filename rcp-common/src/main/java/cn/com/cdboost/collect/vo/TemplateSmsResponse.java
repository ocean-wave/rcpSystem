package cn.com.cdboost.collect.vo;

/**
 * 网易云通知类，运营类短信返回结构
 */
public class TemplateSmsResponse {
    /**
     * code=200表示成功
     */
    private Integer code;
    /**
     * 消息
     */
    private String msg;

    /**
     * code=200时，返回sendid（long型）
     */
    private Long obj;

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Long getObj() {
        return obj;
    }

    public void setObj(Long obj) {
        this.obj = obj;
    }
}
