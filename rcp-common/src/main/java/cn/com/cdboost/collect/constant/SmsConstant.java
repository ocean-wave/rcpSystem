package cn.com.cdboost.collect.constant;

/**
 * 短信相关的常量
 */
public class SmsConstant {

    public enum SmsSchemeType {
        MOBILE_PHONE(1,"手机号码"),
        WEI_XIN(2,"微信");

        /**
         * 类型
         */
        private Integer type;

        /**
         * 描述
         */
        private String desc;

        SmsSchemeType(Integer type, String desc) {
            this.type = type;
            this.desc = desc;
        }

        public Integer getType() {
            return type;
        }

        public void setType(Integer type) {
            this.type = type;
        }

        public String getDesc() {
            return desc;
        }

        public void setDesc(String desc) {
            this.desc = desc;
        }
    }

    /**
     * 系统发送短信的类型
     */
    public enum SmsType {
        ALARM(1,"告警"),
        REMOTE_PAY_SUCCESS(2,"充值到账提醒"),
        PAY_SUCCESS(3,"充值成功"),
        SMS_CODE(4,"验证码");
        /**
         * 类型
         */
        private Integer type;

        /**
         * 描述
         */
        private String desc;

        SmsType(Integer type, String desc) {
            this.type = type;
            this.desc = desc;
        }

        public Integer getType() {
            return type;
        }

        public void setType(Integer type) {
            this.type = type;
        }

        public String getDesc() {
            return desc;
        }

        public void setDesc(String desc) {
            this.desc = desc;
        }
    }

    /**
     * 告警级别
     */
    public enum AlarmLevel {
        ALARM_ONE(1,"告警级别1"),
        ALARM_TWO(2,"告警级别2"),
        ALARM_THREE(3,"告警级别3");

        /**
         * 告警级别
         */
        private Integer level;

        /**
         * 描述
         */
        private String desc;

        AlarmLevel(Integer level, String desc) {
            this.level = level;
            this.desc = desc;
        }

        public Integer getLevel() {
            return level;
        }

        public void setLevel(Integer level) {
            this.level = level;
        }

        public String getDesc() {
            return desc;
        }

        public void setDesc(String desc) {
            this.desc = desc;
        }
    }

}
