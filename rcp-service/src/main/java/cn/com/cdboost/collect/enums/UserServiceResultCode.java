package cn.com.cdboost.collect.enums;

public enum UserServiceResultCode {
    Success(1), // 成功
    Fail(0), // 失败
    LoginUserExists(-1), // 登录名称已经存在
    MobileExists(-2); // 电话号码已经存在

    private int value;

    UserServiceResultCode(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }
}
