package cn.com.cdboost.collect.dto;

import cn.com.cdboost.collect.dto.response.MeterCollectDataFailInfo;

import java.util.List;

/**
 * 实时数据状态查询信息
 */
public class RealTimeDataStatuListInfo {
    /**
     * 状态
     */
    private Integer status;

    /**
     * isUpdate
     */
    private Boolean isUpdate;

    /**
     * 总数
     */
    private Integer total;

    /**
     * 处理数量
     */
    private Integer dealNum;

    /**
     * 未处理数量
     */
    private Integer undealNum;

    /**
     * 成功数量
     */
    private Integer successfulNum;

    /**
     * 失败数量
     */
    private Integer failNum;

    /**
     * 成功召测的列表
     */
    private List<MeterCollectDataListInfo> successList;

    /**
     * cno列表
     */
    private List<String> cnoList;

    /**
     * 失败召测列表
     */
    private List<MeterCollectDataFailInfo> failList;

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Boolean getIsUpdate() {
        return isUpdate;
    }

    public void setIsUpdate(Boolean update) {
        isUpdate = update;
    }

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getDealNum() {
        return dealNum;
    }

    public void setDealNum(Integer dealNum) {
        this.dealNum = dealNum;
    }

    public Integer getUndealNum() {
        return undealNum;
    }

    public void setUndealNum(Integer undealNum) {
        this.undealNum = undealNum;
    }

    public Integer getSuccessfulNum() {
        return successfulNum;
    }

    public void setSuccessfulNum(Integer successfulNum) {
        this.successfulNum = successfulNum;
    }

    public Integer getFailNum() {
        return failNum;
    }

    public void setFailNum(Integer failNum) {
        this.failNum = failNum;
    }

    public List<MeterCollectDataListInfo> getSuccessList() {
        return successList;
    }

    public void setSuccessList(List<MeterCollectDataListInfo> successList) {
        this.successList = successList;
    }

    public List<String> getCnoList() {
        return cnoList;
    }

    public void setCnoList(List<String> cnoList) {
        this.cnoList = cnoList;
    }

    public List<MeterCollectDataFailInfo> getFailList() {
        return failList;
    }

    public void setFailList(List<MeterCollectDataFailInfo> failList) {
        this.failList = failList;
    }
}
