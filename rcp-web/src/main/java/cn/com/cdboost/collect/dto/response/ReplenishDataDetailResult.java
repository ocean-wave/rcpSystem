package cn.com.cdboost.collect.dto.response;

import cn.com.cdboost.collect.vo.PageResult;

/**
 * 工单明细返回给前端的数据结构
 */
public class ReplenishDataDetailResult<T> extends PageResult<T> {
    /**
     * 成功数量
     */
    private Integer successCount;

    /**
     * 失败数量
     */
    private Integer failCount;

    /**
     * 未完成数量
     */
    private Integer unfinishCount;

    public Integer getSuccessCount() {
        return successCount;
    }

    public void setSuccessCount(Integer successCount) {
        this.successCount = successCount;
    }

    public Integer getFailCount() {
        return failCount;
    }

    public void setFailCount(Integer failCount) {
        this.failCount = failCount;
    }

    public Integer getUnfinishCount() {
        return unfinishCount;
    }

    public void setUnfinishCount(Integer unfinishCount) {
        this.unfinishCount = unfinishCount;
    }
}
