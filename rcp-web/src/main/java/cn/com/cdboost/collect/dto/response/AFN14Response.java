package cn.com.cdboost.collect.dto.response;

/**
 * 集中器读取客户档案资料，主动推送给给前端的数据格式
 */
public class AFN14Response {

    /**
     * 集中器地址
     */
    private String jzqNo;

    /**
     * 计量点
     */
    private Integer commSetupSn;

    /**
     * 测量点
     */
    private Integer commPointCode;

    /**
     * 波特率
     */
    private Integer commBaudrate;

    /**
     * 数据采集端口
     */
    private String commPort;

    /**
     * 规约
     */
    private String commRule;

    /**
     * 电表地址
     */
    private String deviceNo;

    /**
     * 费率数
     */
    private Integer feeRateNum;

    /**
     * 采集器地址
     */
    private String cjqNo;

    public String getJzqNo() {
        return jzqNo;
    }

    public void setJzqNo(String jzqNo) {
        this.jzqNo = jzqNo;
    }

    public Integer getCommSetupSn() {
        return commSetupSn;
    }

    public void setCommSetupSn(Integer commSetupSn) {
        this.commSetupSn = commSetupSn;
    }

    public Integer getCommPointCode() {
        return commPointCode;
    }

    public void setCommPointCode(Integer commPointCode) {
        this.commPointCode = commPointCode;
    }

    public Integer getCommBaudrate() {
        return commBaudrate;
    }

    public void setCommBaudrate(Integer commBaudrate) {
        this.commBaudrate = commBaudrate;
    }

    public String getCommPort() {
        return commPort;
    }

    public void setCommPort(String commPort) {
        this.commPort = commPort;
    }

    public String getCommRule() {
        return commRule;
    }

    public void setCommRule(String commRule) {
        this.commRule = commRule;
    }

    public String getDeviceNo() {
        return deviceNo;
    }

    public void setDeviceNo(String deviceNo) {
        this.deviceNo = deviceNo;
    }

    public Integer getFeeRateNum() {
        return feeRateNum;
    }

    public void setFeeRateNum(Integer feeRateNum) {
        this.feeRateNum = feeRateNum;
    }

    public String getCjqNo() {
        return cjqNo;
    }

    public void setCjqNo(String cjqNo) {
        this.cjqNo = cjqNo;
    }
}
