package cn.com.cdboost.collect.dto.response;

/**
 * 重点用户广播返回数据
 */
public class AFN15Response {
    /**
     * 是否关闭弹框状态
     * 0标识执行失败，不能频繁发送广播
     * 1指令投递成功，网络正在执行
     * 2指令执行完成
     */
    private int status;

    /**
     * 已抄到数量
     */
    private int meterReadedTotal;

    /**
     * 抄表总数
     */
    private int meterTotal;

    /**
     * 设备编号
     */
    private String deviceNo;

    /**
     * 设备cno
     */
    private String cno;

    /**
     * 剩余金额
     */
    private String balance;

    /**
     * 购电总额
     */
    private String payMoney;

    /**
     * 购电次数
     */
    private String payCount;

    /**
     * 有功（总）
     */
    private String pr0;

    /**
     * 有功（尖峰）
     */
    private String pr1;

    /**
     * 有功（峰）
     */
    private String pr2;

    /**
     * 有功（平）
     */
    private String pr3;

    /**
     * 有功（谷）
     */
    private String pr4;

    /**
     * A相电流
     */
    private String currentA;

    /**
     * B相电流
     */
    private String currentB;

    /**
     * C相电流
     */
    private String currentC;

    /**
     * A相电压
     */
    private String voltageA;

    /**
     * B相电压
     */
    private String voltageB;

    /**
     * C相电压
     */
    private String voltageC;

    /**
     * 烟雾告警数据
     */
    private String smokeAlarm;

    /**
     * 烟雾报警电池电量
     */
    private String smokeBatteryLevel;

    /**
     * 温度
     */
    private String temperature;

    /**
     * 湿度
     */
    private String humidity;

    /**
     * 光照度
     */
    private String illuminance;

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getMeterReadedTotal() {
        return meterReadedTotal;
    }

    public void setMeterReadedTotal(int meterReadedTotal) {
        this.meterReadedTotal = meterReadedTotal;
    }

    public int getMeterTotal() {
        return meterTotal;
    }

    public void setMeterTotal(int meterTotal) {
        this.meterTotal = meterTotal;
    }

    public String getDeviceNo() {
        return deviceNo;
    }

    public void setDeviceNo(String deviceNo) {
        this.deviceNo = deviceNo;
    }

    public String getCno() {
        return cno;
    }

    public void setCno(String cno) {
        this.cno = cno;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }

    public String getPayMoney() {
        return payMoney;
    }

    public void setPayMoney(String payMoney) {
        this.payMoney = payMoney;
    }

    public String getPayCount() {
        return payCount;
    }

    public void setPayCount(String payCount) {
        this.payCount = payCount;
    }

    public String getPr0() {
        return pr0;
    }

    public void setPr0(String pr0) {
        this.pr0 = pr0;
    }

    public String getPr1() {
        return pr1;
    }

    public void setPr1(String pr1) {
        this.pr1 = pr1;
    }

    public String getPr2() {
        return pr2;
    }

    public void setPr2(String pr2) {
        this.pr2 = pr2;
    }

    public String getPr3() {
        return pr3;
    }

    public void setPr3(String pr3) {
        this.pr3 = pr3;
    }

    public String getPr4() {
        return pr4;
    }

    public void setPr4(String pr4) {
        this.pr4 = pr4;
    }

    public String getCurrentA() {
        return currentA;
    }

    public void setCurrentA(String currentA) {
        this.currentA = currentA;
    }

    public String getCurrentB() {
        return currentB;
    }

    public void setCurrentB(String currentB) {
        this.currentB = currentB;
    }

    public String getCurrentC() {
        return currentC;
    }

    public void setCurrentC(String currentC) {
        this.currentC = currentC;
    }

    public String getVoltageA() {
        return voltageA;
    }

    public void setVoltageA(String voltageA) {
        this.voltageA = voltageA;
    }

    public String getVoltageB() {
        return voltageB;
    }

    public void setVoltageB(String voltageB) {
        this.voltageB = voltageB;
    }

    public String getVoltageC() {
        return voltageC;
    }

    public void setVoltageC(String voltageC) {
        this.voltageC = voltageC;
    }

    public String getSmokeAlarm() {
        return smokeAlarm;
    }

    public void setSmokeAlarm(String smokeAlarm) {
        this.smokeAlarm = smokeAlarm;
    }

    public String getSmokeBatteryLevel() {
        return smokeBatteryLevel;
    }

    public void setSmokeBatteryLevel(String smokeBatteryLevel) {
        this.smokeBatteryLevel = smokeBatteryLevel;
    }

    public String getTemperature() {
        return temperature;
    }

    public void setTemperature(String temperature) {
        this.temperature = temperature;
    }

    public String getHumidity() {
        return humidity;
    }

    public void setHumidity(String humidity) {
        this.humidity = humidity;
    }

    public String getIlluminance() {
        return illuminance;
    }

    public void setIlluminance(String illuminance) {
        this.illuminance = illuminance;
    }
}
