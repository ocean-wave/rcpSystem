package cn.com.cdboost.collect.dto.response;

/**
 * 读转换器时间，返回字段
 */
public class ReadConverterTimeInfo {
    /**
     * 服务器时间
     */
    private String serverTime;

    /**
     * 转换器时间
     */
    private String converterTime;

    public String getServerTime() {
        return serverTime;
    }

    public void setServerTime(String serverTime) {
        this.serverTime = serverTime;
    }

    public String getConverterTime() {
        return converterTime;
    }

    public void setConverterTime(String converterTime) {
        this.converterTime = converterTime;
    }
}
