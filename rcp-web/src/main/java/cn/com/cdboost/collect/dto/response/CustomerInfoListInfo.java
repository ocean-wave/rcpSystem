package cn.com.cdboost.collect.dto.response;

import cn.com.cdboost.collect.dto.param.DeviceNoParam;

import java.util.List;

/**
 * 返回给前端的客户档案信息
 */
public class CustomerInfoListInfo {

    /**
     * 用户状态
     */
    private int isEnabled;

    /**
     * 用户姓名
     */
    private String customerName;

    /**
     * 门牌编号
     */
    private String propertyName;


    /**
     * 联系电话
     */
    private String customerContact;

    /**
     * 用户地址
     */
    private String customerAddr;

    /**
     * 组织名称
     */
    private String orgName;

    /**
     * 创建时间
     */
    private String createTime;

    /**
     * 创建人
     */
    private String createUserName;

    /**
     * 用户唯一标识
     */
    private String customerNo;

    /**
     * 下发状态，1已下发，0未下发
     */
    private Integer sendFlag;

    /**
     * 用户所有水电气表cno拼接，以逗号隔开
     */
    private DeviceNoParam deviceNoParam;

    /**
     * 包含所有水电气表的cno
     */
    private List<String> cnoList;

    /**
     * 添加品牌
     */
    private String customerBrand;

    public int getIsEnabled() {
        return isEnabled;
    }

    public void setIsEnabled(int isEnabled) {
        this.isEnabled = isEnabled;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getPropertyName() {
        return propertyName;
    }

    public void setPropertyName(String propertyName) {
        this.propertyName = propertyName;
    }

    public String getCustomerContact() {
        return customerContact;
    }

    public void setCustomerContact(String customerContact) {
        this.customerContact = customerContact;
    }

    public String getCustomerAddr() {
        return customerAddr;
    }

    public void setCustomerAddr(String customerAddr) {
        this.customerAddr = customerAddr;
    }

    public String getOrgName() {
        return orgName;
    }

    public void setOrgName(String orgName) {
        this.orgName = orgName;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getCreateUserName() {
        return createUserName;
    }

    public void setCreateUserName(String createUserName) {
        this.createUserName = createUserName;
    }

    public String getCustomerNo() {
        return customerNo;
    }

    public void setCustomerNo(String customerNo) {
        this.customerNo = customerNo;
    }

    public Integer getSendFlag() {
        return sendFlag;
    }

    public void setSendFlag(Integer sendFlag) {
        this.sendFlag = sendFlag;
    }

    public DeviceNoParam getDeviceNoParam() {
        return deviceNoParam;
    }

    public void setDeviceNoParam(DeviceNoParam deviceNoParam) {
        this.deviceNoParam = deviceNoParam;
    }

    public List<String> getCnoList() {
        return cnoList;
    }

    public void setCnoList(List<String> cnoList) {
        this.cnoList = cnoList;
    }

    public String getCustomerBrand() {
        return customerBrand;
    }

    public void setCustomerBrand(String customerBrand) {
        this.customerBrand = customerBrand;
    }

}
